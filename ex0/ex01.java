package ex0;

public class ex01 {

	public static void main(String[] args) {
		byte a = 011;			// 1byte
		short b = 30000;	// 2byte			
		int c = 100000000;	// 4byte			
		long l = 1000l; 	// 8byte			
		char d = 'G';		// 2byte			
		boolean e = true; 		
		System.out.println(a); 		
		System.out.println(b); 		
		System.out.println(c);		
		System.out.println((int)d);		
		System.out.println(e);			
		System.out.println(l);			
	}

}
	